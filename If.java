public class If {
    public static void main(String args[]){
        int age=2;
        if(age>7){
            System.out.println("it is true");
        }else{
            System.out.println("it is false");
        }
        int q=13;
        switch(q){
            case 1:System.out.println("it is one");
            break;
            case 8:System.out.println("it is eight");
            break;
            case 12:System.out.println("it is tweleven");
            break;
            default:System.out.println("it is false");
        }
    }
    
}
