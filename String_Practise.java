public class String_Practise {
    public static void main(String args[]){
        String a="priyanka";
        System.out.println(a.length());
        System.out.println(a.toLowerCase());
        System.out.println(a.toUpperCase());
        System.out.println(a.substring(3));
        System.out.println(a.substring(2,4));
        System.out.println(a.replace('r', 'z'));
        System.out.println(a.replace("riy", "yir"));
        System.out.println(a.startsWith("p"));
        System.out.println(a.endsWith("ka"));
        System.out.println(a.equals("priyan"));
        System.out.println("Hello sir,\t I am anuj\n i read in class 10");

    }
    
}
