public class Loop {
    public static void main(String args[]){
        int i;
        for(i=1;i<=5;i++){
            System.out.println(i);
        }
        System.out.println("it is for looop");
        int a=1;
        while(a<=6){
            System.out.println(a+2);
            a++;
        }
        System.out.println("it is while loop");
        int b=1;
        do{
          
            System.out.println(b);
            b++;
        }while(b<=4);
    }
    
}
